func TestParse(t *testing.T) {
	str := "iscjBInoAAA="
	str1 := "AQ=="
	fmt.Println(ParseU64(str))
	fmt.Println(ParseU64(str1))
}

func TestParseOrderId(t *testing.T) {
	str := "Dg=="
	str1 := "Dw=="
	ret := ParseU64(str)
	ret2 := ParseU64(str1)

	assert.Equal(t, ret, "14", "The two words should be the same.")
	assert.Equal(t, ret, "15", "The two words should be the same.")
	fmt.Println(ret, ret2)
}

